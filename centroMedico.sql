create database centroMedico;
use centroMedico;
create table paciente(
	idPaciente int NOT NULL PRIMARY KEY,
    nombre varchar(50) NOT NULL,
    apellido varchar(50) NULL,
    fnacimiento date NULL,
    domicilio varchar(50) NULL,
    idpais char(3) NULL,
    telefono varchar(20) NULL,
    email varchar(30) NULL,
    observacion varchar(1000) NULL
    );
create table Turno(
	idTurno int NOT NULL PRIMARY KEY,
    fechaTurno datetime NULL,
    estado varchar(50) NULL,
    observacion varchar(500) NULL
);
create table Pais(
	idPais char(3) NOT NULL PRIMARY KEY,
    Pais varchar(30) NULL
);
create table Pago(
	idPago int NOT NULL PRIMARY KEY,
    concepto varchar(30) NULL,
    fecha date NULL,
    monto int NULL,
    estado varchar(30) NULL,
    obs varchar(1000) NULL
);
create table Medico(
	idMedico int NOT NULL PRIMARY KEY,
    nombre varchar(50) NULL,
    apellido varchar(50) NULL
);
create table Historia(
	idHistoria int NOT NULL PRIMARY KEY,
    fechaHistoria date NULL,
    observacion varchar(1000) NULL
);
create table Especialidad(
	idEspecialidad int NOT NULL PRIMARY KEY,
    Especialidad varchar(30) NULL
);
create table TurnoPaciente(
	idTurno int NOT NULL,
    idPaciente int NOT NULL,
    idMedico int NOT NULL
);
create table TurnoEstado(
	idEstado varchar(50) NULL,
    descripcion varchar(50) NULL
);
create table PagoPaciente(
	idPago int NOT NULL,
    idPaciente int NOT NULL,
    idTurno int NOT NULL
);
create table MedicoEspecialidad(
	idMedico int NOT NULL,
    idEspecialidad int NOT NULL,
    descripcion varchar(50) NULL
);
create table HistoriaPaciente(
	idHistoria int NOT NULL,
    idPaciente int NOT NULL,
    idMedico int NOT NULL
);

